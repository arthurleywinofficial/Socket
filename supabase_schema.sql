-- SOCKET Industrial Platform - Supabase Schema v1.1 (Facility & Storage Expansion)

-- 1. Tesisler (Harita Entegrasyonu)
CREATE TABLE IF NOT EXISTS public.facilities (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    type TEXT NOT NULL, -- Rafineri, Terminal, Depo
    latitude FLOAT8 NOT NULL,
    longitude FLOAT8 NOT NULL,
    status TEXT DEFAULT 'Aktif', -- Aktif, Bakım, Kritik
    manager_id UUID REFERENCES auth.users(id),
    last_inspection TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now())
);

-- 2. Raporlar (Metadata)
CREATE TABLE IF NOT EXISTS public.reports (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()),
    title TEXT NOT NULL,
    category TEXT NOT NULL, -- Güvenlik, Verimlilik, Çevre
    file_path TEXT NOT NULL, -- Storage üzerindeki yol
    created_by UUID REFERENCES auth.users(id),
    priority TEXT DEFAULT 'Normal'
);

-- 3. Personel Belgeleri (Metadata)
CREATE TABLE IF NOT EXISTS public.personnel_docs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID REFERENCES auth.users(id) ON DELETE CASCADE,
    doc_type TEXT NOT NULL, -- İSG Sertifikası, Ehliyet, Kimlik
    file_path TEXT NOT NULL,
    expiry_date DATE,
    verified BOOLEAN DEFAULT false
);

-- GÜVENLİK: RLS Aktifleştirme
ALTER TABLE public.facilities ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.reports ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.personnel_docs ENABLE ROW LEVEL SECURITY;

-- Politikalar
CREATE POLICY "Herkes tesisleri görebilir" ON public.facilities FOR SELECT USING (true);
CREATE POLICY "Herkes raporları görebilir" ON public.reports FOR SELECT USING (true);
CREATE POLICY "Kullanıcılar sadece kendi belgelerini görebilir" ON public.personnel_docs FOR SELECT USING (auth.uid() = user_id);

